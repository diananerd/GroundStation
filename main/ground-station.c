void app_main(void) {
  /* Log git version build */
  const esp_app_desc_t* app_description;
  app_description = esp_app_get_description();

  ESP_LOGI(TAG, "Build version %s", app_description->version);
  screen_init();
  screen_clear();
  screen_draw(gs_logo);

  char data_str[80] = {0};
  sprintf(data_str, "    v%s", app_description->version);
  screen_print(data_str, 7);

  /* Prepare serial console for REPL */
  esp_console_repl_t *repl = NULL;
  esp_console_repl_config_t repl_config = ESP_CONSOLE_REPL_CONFIG_DEFAULT();
  repl_config.task_stack_size = (1024 * 8);
  repl_config.prompt = ">";

  initialize_nvs();
  initialize_wifi();
  initialize_api();

  nvs_session_init();

  /* Register commands */
  esp_console_register_help_command();
  register_wifi();
  register_api();

  /* Setup console REPL over UART */
  esp_console_dev_uart_config_t hw_config = ESP_CONSOLE_DEV_UART_CONFIG_DEFAULT();
  ESP_ERROR_CHECK(esp_console_new_repl_uart(&hw_config, &repl_config, &repl));

  /* Run REPL */
  ESP_ERROR_CHECK(esp_console_start_repl(repl));

  xTaskCreate(&ota_task, "ota_task", 1024 * 8, NULL, 5, NULL);
}
